void solve(){
	ll n,m;
	cin >> n >> m;
	if(m < n || n*(n+1)/2 < m){
		cout << -1 << '\n';
		return;
	}
	
	ll sum = 0;
	int prev = 0;
	for(ll i = n;i;i--,prev++){
		if(sum+i >= m){
			cout << prev+1 << '\n';
			int y = (n-i+3)*(prev+1)-(m-(1ll*prev*(prev-1)/2));
			int x = (n-i+3)-y;
			x--,y--;
			int j = n;
			for(int k = prev;k>=1;k--){
				cout << k+1 << " " << k << '\n';
			}

			for(int k = 0 ;k<x;k++,j--){
				cout << prev+1 << " " << j << '\n';
			}
			for(int k = 0;k<y;k++,j--){
				cout << prev << " " << j << '\n';
			}
			return;
		}
		sum += i;
	}


}


